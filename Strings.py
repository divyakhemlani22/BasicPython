# converting to lower case
print(phrase.lower())

# converting to uppsercase
print(phrase.upper())

# Checking if it is fully lowercase or uppercase
print(phrase.isupper())

# You can convert it to upper first then check if its upper or not
print(phrase.upper().isupper())

# length of the phrase
